import type { GetServerSideProps } from 'next'
import Head from 'next/head'
import styles from '../../styles/Home.module.css'
import FlightDetails from '../../components/FlightDetails'
import { Launch } from '../../types/FlightListTypes';

interface LaunchDetailsPageProps {
  data: Launch;
}

const LaunchDetailsPage = ({ data }: LaunchDetailsPageProps) => {
  return (
    <div className={styles.container}>
      <Head>
        <title>SpaceX Launches - list</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main className={styles.main}>
        <h1 className={styles.title}>
          SpaceX Launch {data.mission_name}
        </h1>

        <FlightDetails />
      </main>

      <footer className={styles.footer}>
        <a
          href="https://www.omnicalculator.com/"
          target="_blank"
          rel="noopener noreferrer"
        >
          Created by Damian Stycze≈Ñ for Omni Calculator
        </a>
      </footer>
    </div>
  )
}

export const getServerSideProps: GetServerSideProps = async (context) => {
    const response = await fetch(`https://api.spacexdata.com/v3/launches/${context?.params?.id}`);
    const data = await response.json();
  
    return {
      props: {
        data
      }
    }
  }

export default LaunchDetailsPage
